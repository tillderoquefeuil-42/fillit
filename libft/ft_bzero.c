#include "libft.h"

void		ft_bzero(void *s, size_t n)
{
	unsigned int	i;
	unsigned char	*set_s;

	i = 0;
	set_s = s;
	while (i < n)
	{
		set_s[i] = 0;
		i++;
	}
}
